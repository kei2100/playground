public class CreatePoolEntryException extends Exception{

	private static final long serialVersionUID = 678424968631025325L;

	public CreatePoolEntryException() {
		super();
	}

	public CreatePoolEntryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CreatePoolEntryException(String arg0) {
		super(arg0);
	}

	public CreatePoolEntryException(Throwable arg0) {
		super(arg0);
	}
}
